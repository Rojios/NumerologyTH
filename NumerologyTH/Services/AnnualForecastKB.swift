// MARK: - Convenience API

extension AnnualForecastKB {

    /// ดึง full forecast text สำหรับ user
    /// - Parameters:
    ///   - dayMasterElement: ธาตุ Day Master ของ user
    ///   - yearElement: ธาตุของปีปัจจุบัน (จาก BaZiEngine.yearPillar)
    ///   - isClash: นักษัตรของ user ชงกับปีปัจจุบันหรือไม่
    /// - Returns: บทความพร้อมแสดงผล (base + modifier ถ้าชง)
    static func forecast(
        dayMasterElement: Element,
        yearElement: Element,
        isClash: Bool
    ) -> String {
        guard let base = base[dayMasterElement]?[yearElement] else {
            return "ไม่พบข้อมูลดวงปีนี้ 🐱"
        }

        var result = base.article

        if isClash, let modifier = clashModifier[dayMasterElement] {
            result += "\n\n" + modifier.modifier
        }

        return result
    }

    /// ดึง header text สำหรับแสดงก่อน article
    /// - Parameters:
    ///   - yearElementName: ชื่อธาตุปี เช่น "ไฟ"
    ///   - yearAnimalThai: นักษัตรปี เช่น "มะเมีย"
    ///   - yearStemChar: อักษรจีน Stem เช่น "丙"
    ///   - yearBranchChar: อักษรจีน Branch เช่น "午"
    ///   - isClash: ชงหรือไม่
    ///   - elementRelationLabel: ความสัมพันธ์ธาตุ เช่น "ไฟพบไฟ"
    ///   - overallTier: ภาพรวม
    /// - Returns: header string พร้อมแสดงผล
    static func header(
        yearElementName: String,
        yearAnimalThai: String,
        yearStemChar: String,
        yearBranchChar: String,
        isClash: Bool,
        elementRelationLabel: String,
        overallSummary: String
    ) -> String {
        let clashLine = isClash
            ? "🐯 ปีนักษัตร : ⚠️ ชงกับปีเกิดของคุณโดยตรง ต้องระวังเป็นพิเศษ"
            : "🐯 ปีนักษัตร : ✅ ไม่ชง ปีเอื้ออำนวย"

        return """
🔥 ธาตุปีนี้: \(yearElementName) (\(yearStemChar)\(yearBranchChar)) ⭐ นักษัตรปีนี้: \(yearAnimalThai)

\(clashLine)
🔥 ธาตุปี ⚡ \(elementRelationLabel)

📊 ภาพรวม \(overallSummary)
"""
    }
}
